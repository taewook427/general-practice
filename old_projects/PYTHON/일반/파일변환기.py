            nonlocal win
            if re == 0:
                status.set('worked successfully')
            else:
                status.set( 'order failed with ' + str(re) )
                time.sleep(0.1)
                tkinter.messagebox.showinfo( 'ERR ' + str(re),' CMD order was : \n' + order + '\n ' )
